package assignments6;
class ColorDescription
{
	public static void main(String args[])
	{
	String name="Red";
	switch(name)
	{
		case "Red":
			System.out.println("Power and Danger");
			break;
		case "Pink":
			System.out.println("Happiness and Feminine");
			break;
		case "Orange":
			System.out.println("Encouragement and Vitality");
			break;
		case "Brown":
			System.out.println("Honest");
			break;
		case "Green":
			System.out.println("Earth");
			break;
		case "White":
			System.out.println("Purity and Clean");
			break;
	}
	}
}