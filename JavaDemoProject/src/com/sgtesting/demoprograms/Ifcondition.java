package com.sgtesting.demoprograms;

public class Ifcondition {

	public static void main(String[] args) {
		char ch='p';
		switch(ch){
		case 'A':
			System.out.println("it is a vowel");
			break;
		case 'E':
			System.out.println("it is a vowel");
			break;
		case 'I':
			System.out.println("it is a vowel");
			break;
		case 'O':
			System.out.println("it is a vowel");
			break;
		case 'U':
			System.out.println("it is a vowel");
			break;
		default:
			System.out.println("it is not a vowel");
		}
		
	}

}
