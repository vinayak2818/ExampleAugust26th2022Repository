package sg.testing.Practice;
class Program
{
	void Program1(String name)
	{
		System.out.println(name);
	}
	void Program1()
	{
		System.out.println("c++");
		
	}
}

public class MethodOverloading {

	public static void main(String[] args) {
		Program n=new Program();
		n.Program1();
		n.Program1("oracle");
		
		

	}

}
